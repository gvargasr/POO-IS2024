  private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
    // TODO add your handling code here:
    String codigo = (String) jComboBox2.getSelectedItem();
    if (codigo != null && !codigo.isEmpty()) {
      if (verificarCodigo(codigo)) {
        if (jComboBox4.getSelectedItem() != null
            && !jComboBox4.getSelectedItem().toString().isEmpty()) {
          JOptionPane.showMessageDialog(this, "Registro exitoso", "Éxito",
              JOptionPane.INFORMATION_MESSAGE);
          // Lógica adicional para registrar Requisito
          controlador.RegistrarRequisitoController.eventoBotonRegistrarRequisito();
        } else {
          JOptionPane.showMessageDialog(this, "Seleccione código válido", "Error",
              JOptionPane.ERROR_MESSAGE);
        }
      } else {
        JOptionPane.showMessageDialog(this, "Codigo ya existente", "Error",
            JOptionPane.ERROR_MESSAGE);
      }
    } else {
      JOptionPane.showMessageDialog(this, "Seleccione código válido", "Error",
          JOptionPane.ERROR_MESSAGE);
    }
  }// GEN-LAST:event_jButton1ActionPerformed


  private boolean verificarCodigo(String codigo) {
    Escuela escuelaActual =
        VistaPrincipalController.modelo.buscarEscuela((String) jComboBox1.getSelectedItem());
    Curso cursoActual = escuelaActual.buscarCurso((String) jComboBox4.getSelectedItem());
    ArrayList<Curso> listaRequisitos = cursoActual.getRequisitos();
    ArrayList<Curso> listaCorrequisitos = cursoActual.getCorrequisitos();
    for (Curso curso : listaRequisitos) {
      if (curso.getCodigo().equals(codigo)) {
        return false;
      }
    }
    for (Curso curso : listaCorrequisitos) {
      if (curso.getCodigo().equals(codigo)) {
        return false;
      }
    }
    return true;
  }

