  public void updateCursos(ArrayList<Curso> listaCursos) {
    jComboBox4.removeAllItems();
    for (Curso curso : listaCursos) {
      jComboBox4.addItem(curso.getCodigo());
    }
  }

  public void updateRequisitos(ArrayList<Curso> listaCursos) {
    jComboBox2.removeAllItems();
    jComboBox3.removeAllItems();
    for (Curso curso : listaCursos) {
      if (!curso.getCodigo().equals(jComboBox4.getSelectedItem())) {
        jComboBox2.addItem(curso.getCodigo());
        jComboBox3.addItem(curso.getCodigo());
      }
    }
  }

